export function getTranslatedString(key: string, lang: Language, options?: { [key: string]: string | number }): string {
  let str = translations[key]?.[lang] || translations[key]?.['en'] || key;
  if (options) {
    Object.keys(options).forEach(optKey => {
      str = str.replace(`{${optKey}}`, String(options[optKey]));
    });
  }
  return str;
}

